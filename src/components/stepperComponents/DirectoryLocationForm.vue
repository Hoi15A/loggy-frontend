<template>
  <v-form>
    <v-container>
      <v-select
          v-model="location"
          :items="['Local', 'Remote']"
          label="Log Service Location"/>
      <v-row>
        <v-col>
          <v-treeview
              v-model="selection"
              :items="items"
              :load-children="fetchSubFolders"
              selectable
              return-object/>
        </v-col>
        <v-divider vertical/>
        <v-col
            class="pa-6"
            cols="6">
          <template v-if="!selection.length">
            No nodes selected.
          </template>
          <template v-else>
            <div
                v-for="node in selection"
                :key="node.id">
              {{ node.fullpath }}
            </div>
          </template>
        </v-col>
      </v-row>
    </v-container>
  </v-form>
</template>

<script lang="ts">
import PathApi from "@/api/pathApi";
import Vue from "vue";

export default Vue.extend({
  data: () => ({
    location: "Local", // local (1), remote (0)
    selection: [],
    items: [],
  }),

  async beforeMount() {
    this.items = await this.fetchRootFolder();
  },

  methods: {
    async fetchRootFolder() {
      try {
        const res = await PathApi.getRootOfLocalServer();
        return await res.json();
      } catch (err) {
        console.warn(err);
        return [];
      }
    },

    async fetchSubFolders(item: any) {
      try {
        const res = await PathApi.getContentOfFolder(item.fullpath);
        item.children = await res.json();
      } catch (err) {
        console.warn(err);
      }
    },
  },
});
</script>

